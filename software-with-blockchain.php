          <div class="col-12">
            <div class="row align-items-center">
              <div class="col-lg-6">
                <h3>Key Features of Blockchain Software Systems</h3>
                <ul>
                  <li><strong>Decentralization:</strong>
                    <ul>
                      <li>Data is distributed across multiple nodes.</li>
                      <li>Eliminates the need for a central authority.</li>
                    </ul>
                  </li>

                  <li><strong>Transparency:</strong>
                    <ul>
                      <li>All transactions are recorded on a public ledger.</li>
                      <li>Enhances accountability and trust.</li>
                    </ul>
                  </li>

                  <li><strong>Security:</strong>
                    <ul>
                      <li>Utilizes cryptographic techniques to secure data.</li>
                      <li>Resistant to tampering and fraud.</li>
                    </ul>
                  </li>

                  <li><strong>Immutability:</strong>
                    <ul>
                      <li>Once recorded, data cannot be altered or deleted.</li>
                      <li>Ensures data integrity.</li>
                    </ul>
                  </li>

                  <li><strong>Consensus Mechanisms:</strong>
                    <ul>
                      <li>Validates transactions through consensus protocols (e.g., Proof of Work, Proof of Stake).</li>
                      <li>Ensures agreement across all nodes.</li>
                    </ul>
                  </li>

                  <li><strong>Smart Contracts:</strong>
                    <ul>
                      <li>Self-executing contracts with predefined rules.</li>
                      <li>Automates and enforces contractual agreements.</li>
                    </ul>
                  </li>
                </ul>
              </div>

